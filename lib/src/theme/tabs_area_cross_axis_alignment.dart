/// Defines the alignment of tabs in relation to the main content.
enum TabsAreaCrossAxisAlignment {
  /// Aligns the tabs to the edge of the tabs area closest to the main content.
  inner,

  /// Centers the tabs within the tabs area, aligning them equally between
  /// the inner and outer edges.
  center,

  /// Aligns the tabs to the outermost edge of the tabs area, positioning
  /// them away from the main content.
  outer,
}
